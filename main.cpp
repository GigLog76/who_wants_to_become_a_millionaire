struct Question{
    int id;
    int level;
    std::string question;
    std::array<std::string, 4> options;
    int correct;

    Question() : id(0), level(1), correct(0) {
        options.fill("");
    }


    Question(int i, int lvl, const std::string& q, 
             const std::array<std::string, 4>& opts, int c)
        : id(i), level(lvl), question(q), options(opts), correct(c) {}

    bool isCorrect(char AnswerIndex) const {

        return AnswerIndex-65 == correct;
    }


    void print (){
        std::cout<<question<<std::endl;
        for (int i = 0; i < 4; ++i) {
            std::cout << "  " << char('A' + i) << ") " << options[i] << "\n";
        }
    }

};

