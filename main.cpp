size_t CountQuestions = questions.size()/5;

void GameProcess(){
    std::random_device rd;
    std::mt19937 gen(rd());
    while ((Stage != 15) && (Lose!=1)){
        

        

        std::uniform_int_distribution<int> dist(QuestionDifficult*CountQuestions, CountQuestions+QuestionDifficult*CountQuestions-1);

        int randomIndex = dist(gen);
        auto ActualQuestion = questions[randomIndex];
        ActualQuestion.print();
        char choice;
        std::cin>>choice;
        if (choice=='Q'){ 
            Prize = prizeTable[Stage];
            return;
        }
        bool answer = ActualQuestion.isCorrect(choice);

        if (answer == 1){
            Stage++;
            Prize = prizeTable[Stage];
            if (Stage%3==0){
                QuestionDifficult++;
            }
            
        }
        else{
            Lose = 1;
            if (Stage >= 10) Prize = 100000;
            else if (Stage >= 5) Prize = 5000;
            else Prize = 0;
        }

    }
}

